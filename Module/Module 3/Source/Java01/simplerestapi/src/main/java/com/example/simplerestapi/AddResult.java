package com.example.simplerestapi;

public class AddResult {
    private int a, b, c; 

    public AddResult(int a, int b, int c) {
        this.a = a;
        this.b = b;
        this.c = c;
    }

    public int getA() {
        return a;
    }

    public int getB() {
        return b;
    }

    public int getC() {
        return c;
    }
}
